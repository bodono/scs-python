def _info_summary(info):
    """Return a compact string of solver info fields useful for diagnosing failures."""
    keys = ("status", "status_val", "iter", "res_pri", "res_dual", "gap",
            "pobj", "dobj", "res_infeas", "res_unbdd_a", "res_unbdd_p", "scale")
    return ", ".join(f"{k}={info.get(k)}" for k in keys if k in info)


@_AA_PARAMS
def test_warm_start_first_call_not_infeasible(acceleration_lookback):
    """First warm-start call must not return 'infeasible' for a feasible problem."""
    data, cones = _make_problem()
    kwargs = {**_SOLVER_KWARGS, "acceleration_lookback": acceleration_lookback}
    solver = scs.SCS(data, cones, **kwargs)
    sol = solver.solve(warm_start=True, x=_x0.copy(), y=_y0.copy(), s=_s0.copy())
    assert sol["info"]["status"] != "infeasible", (
        f"First warm-start solve incorrectly returned 'infeasible' "
        f"(acceleration_lookback={acceleration_lookback}); "
        "expected 'solved' (regression for GitHub issue #350). "
        f"Solver info: {_info_summary(sol['info'])}"
    )


@_AA_PARAMS
def test_warm_start_consistent_with_cold_start(acceleration_lookback):
    """Warm-start and cold-start solves of the same problem must agree on status."""
    data, cones = _make_problem()
    kwargs = {**_SOLVER_KWARGS, "acceleration_lookback": acceleration_lookback}

    solver_warm = scs.SCS(data, cones, **kwargs)
    sol_warm1 = solver_warm.solve(warm_start=True, x=_x0.copy(), y=_y0.copy(), s=_s0.copy())
    sol_warm2 = solver_warm.solve(warm_start=True, x=_x0.copy(), y=_y0.copy(), s=_s0.copy())

    solver_cold = scs.SCS(data, cones, **kwargs)
    sol_cold = solver_cold.solve()

    assert sol_cold["info"]["status"] == "solved", (
        f"Cold-start solve unexpectedly did not return 'solved' "
        f"(acceleration_lookback={acceleration_lookback}). "
        f"Solver info: {_info_summary(sol_cold['info'])}"
    )
    assert sol_warm1["info"]["status"] == sol_cold["info"]["status"], (
        f"First warm-start status '{sol_warm1['info']['status']}' differs from "
        f"cold-start status '{sol_cold['info']['status']}' "
        f"(acceleration_lookback={acceleration_lookback}). "
        f"Warm-start info: {_info_summary(sol_warm1['info'])}"
    )
    assert sol_warm2["info"]["status"] == sol_cold["info"]["status"], (
        f"Second warm-start status '{sol_warm2['info']['status']}' differs from "
        f"cold-start status '{sol_cold['info']['status']}' "
        f"(acceleration_lookback={acceleration_lookback}). "
        f"Warm-start info: {_info_summary(sol_warm2['info'])}"
    )


