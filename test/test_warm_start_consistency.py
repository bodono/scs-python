def _make_problem():
    P = sp.csc_matrix(
        (np.array(_P_data), np.array(_P_indices), np.array(_P_indptr)),
        shape=(15, 15),
    )
    G = sp.csc_matrix(
        (np.array(_G_data), np.array(_G_indices), np.array(_G_indptr)),
        shape=(60, 15),
    )
    data = {"P": P, "A": G, "b": _h.copy(), "c": _q.copy()}
    cones = {"l": 60}
    return data, cones


